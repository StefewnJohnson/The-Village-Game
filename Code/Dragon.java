public class Dragon {

  int damage; // initiates damage
  int hp; // initiates hp
  int xp; // initiates xp

  public Dragon(int damage, int hp, int xp) {
    this.damage = damage; // sets dragon damage equal to declared dragon damage
    this.hp = hp; // sets dragon hp equal to declared dragon hp
    this.xp = xp; // sets dragon xp equal to declared dragon xp
  }

}